     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $user = Auth::user()->id;
        $posts = new post();
        $posts->users_id    = $user;
        $posts->post_text   = $request->post_content;

        $posts->save();
        return redirect('blog');

